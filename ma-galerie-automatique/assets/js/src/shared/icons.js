export const sanitizeIconKey = (value) => {
    if (typeof value !== 'string') {
        return '';
    }

    return value.trim().toLowerCase().replace(/[^a-z0-9_-]/g, '');
};

const getDefinition = (iconKey) => {
    const sanitizedKey = sanitizeIconKey(iconKey);

    if (sanitizedKey && SHARE_ICON_LIBRARY[sanitizedKey]) {
        return SHARE_ICON_LIBRARY[sanitizedKey];
    }

    return SHARE_ICON_LIBRARY.generic;
};

export const createSvgElement = (doc, tag, attributes = {}) => {
    if (!doc || typeof doc.createElementNS !== 'function') {
        return null;
    }

    const element = doc.createElementNS('http://www.w3.org/2000/svg', tag);

    Object.keys(attributes).forEach((attribute) => {
        element.setAttribute(attribute, attributes[attribute]);
    });

    return element;
};

export const createIconSvg = (doc, iconKey) => {
    const definition = getDefinition(iconKey);
    const svg = createSvgElement(doc, 'svg', {
        viewBox: definition.viewBox,
        fill: 'currentColor',
        'aria-hidden': 'true',
        focusable: 'false',
    });

    if (!svg) {
        return null;
    }

    if (Array.isArray(definition.paths)) {
        definition.paths.forEach((pathDefinition) => {
            const path = createSvgElement(doc, 'path', pathDefinition);

            if (path) {
                svg.appendChild(path);
            }
        });
    }

    return svg;
};

export const createIconPreview = (doc, iconKey, { wrapperTag = 'span', wrapperClass = '' } = {}) => {
    if (!doc || typeof doc.createElement !== 'function') {
        return null;
    }

    const wrapper = doc.createElement(wrapperTag);

    if (wrapperClass) {
        wrapper.className = wrapperClass;
    }

    const svg = createIconSvg(doc, iconKey);

    if (!svg) {
        return null;
    }

    wrapper.appendChild(svg);

    return wrapper;
};

